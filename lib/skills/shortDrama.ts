/**
 * 生成人物档案注入文本（用于分镜 prompt，确保跨镜头外貌一致性）
 */
export function buildCharacterProfiles(analysis?: NovelAnalysis | null): string {
  if (!analysis?.characters?.length) return "";
  const lines = analysis.characters.map((c) => {
    const parts = [`- ${c.name}（${c.gender ?? ""}${c.age ? "，" + c.age : ""}）`];
    if (c.appearance) parts.push(`五官：${c.appearance}`);
    if (c.temperament) parts.push(`气质：${c.temperament}`);
    if (c.style) parts.push(`穿搭：${c.style}`);
    if (c.traits?.length) parts.push(`性格标签：${c.traits.join("、")}`);
    return parts.join("；");
  });
  return `\n【角色外貌固定档案（必须严格遵守，每个镜头出现该角色时附上其外貌关键词）】\n${lines.join("\n")}\n`;
}

/**
 * 提取固定核心场景信息（用于剧本生成 user prompt）
 */
export function buildAnchorSceneHint(analysis: NovelAnalysis): string {
  const anchors = (analysis.scenes as (typeof analysis.scenes[0] & { sceneType?: string; landmarkElement?: string })[])
    .filter((s) => s.sceneType === "anchor")
    .map((s) => `「${s.location}」（标志性元素：${s.landmarkElement ?? "无"}）`)
    .join("、");
  return anchors ? `\n【固定核心场景（本集需出现并体现标志性元素）】${anchors}` : "";
}
